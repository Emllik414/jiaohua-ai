export const SKILL_ICON_KEYS = Object.keys(ICON_PATHS)

/**
 * 根据技能 id / name 推断默认 iconKey（供旧数据迁移和新技能使用）
 */
export function getDefaultSkillIconKey(skillId: string, skillName?: string): string {
  const id = String(skillId || '').toLowerCase().trim()
  const name = String(skillName || '').toLowerCase().trim()

  const mapping: [string, string][] = [
    ['copy', 'copy'],
    ['smart_translate', 'translate'],
    ['translate', 'translate'],
    ['explain', 'chat'],
    ['lookup', 'search'],
    ['dictionary', 'search'],
    ['summarize', 'list'],
    ['summary', 'list'],
    ['xiaohongshu', 'heart'],
    ['obsidian', 'obsidian'],
    ['import_obsidian', 'obsidian'],
    ['pronunciation', 'speaker'],
    ['speak', 'speaker'],
    ['read', 'book'],
  ]

  for (const [key, iconKey] of mapping) {
    if (id === key || name.includes(key)) return iconKey
  }

  // 旧 Unicode icon 映射
  const unicodeMapping: [string, string][] = [
    ['📋', 'copy'],
    ['◇', 'translate'],
    ['译', 'translate'],
    ['i', 'search'],
    ['≡', 'list'],
    ['书', 'obsidian'],
    ['♪', 'speaker'],
    ['♡', 'heart'],
    ['✎', 'pen'],
    ['⌕', 'search'],
    ['自', 'spark'],
  ]

  for (const [char, iconKey] of unicodeMapping) {
    if (id.includes(char) || name.includes(char)) return iconKey
  }

  return 'spark'
}

type SkillIconProps = {
  iconKey?: string
  className?: string
}

export function SkillIcon({ iconKey, className }: SkillIconProps) {
  const key = (iconKey && ICON_PATHS[iconKey]) ? iconKey : 'spark'
  return (
    <span className={className} style={{ display: 'inline-flex', alignItems: 'center', justifyContent: 'center', width: '1em', height: '1em' }}>
      {ICON_PATHS[key]}
    </span>
  )
}
